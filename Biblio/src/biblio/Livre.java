    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            Class.forName("org.sqlite.JDBC");
            con=DriverManager.getConnection("jdbc:sqlite:biblio.db");
            String sql="insert into livre(id_livre,titre,langue,isbn,annee,page,date_achat,date_parution,id_auteur) values(?,?,?,?,?,?,?,?,?)";
            pst=con.prepareStatement(sql);
            pst.setString(1,txtidli.getText());
            pst.setString(2,txttitre.getText());
            pst.setString(3,txtlangue.getText());
            pst.setString(4,txtisbn.getText());
            pst.setString(5,txtannee.getText());
            pst.setString(6,txtnb.getText());
            pst.setString(7,txtachat.getText());
            pst.setString(8,txtparution.getText());
            pst.setString(9,txtid.getText());
            pst.executeUpdate();
            con.close();
            JOptionPane.showMessageDialog(null,"Enregistrement reussi");
            TableLivre();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

