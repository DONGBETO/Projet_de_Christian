    public void TableLivre(){
        String [] livre={"ID","titre","langue-livre","isbn","annee","nb-page","date-achat","date-parution","id-auteur:"};
        String [] afficher=new String[10];
        DefaultTableModel model=new DefaultTableModel(null,livre);
        String sql="select * from livre";
        
        try {
            Class.forName("org.sqlite.JDBC");
            con=DriverManager.getConnection("jdbc:sqlite:biblio.db");
            Statement st=con.createStatement();
            rs=st.executeQuery(sql);
            
            while (rs.next()) {                
                afficher[0]=rs.getString("id_livre");
                afficher[1]=rs.getString("titre");
                afficher[2]=rs.getString("langue");
                afficher[3]=rs.getString("isbn");
                afficher[4]=rs.getString("annee");
                afficher[5] = rs.getString("page");
                afficher[6] = rs.getString("date_achat");
                afficher[7] = rs.getString("date_parution");
                afficher[8] = rs.getString("id_auteur");
                model.addRow(afficher);
            }
            jTable1.setModel(model);
            con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
